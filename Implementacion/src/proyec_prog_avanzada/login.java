    private boolean validarUsuarios(String usuario, String clave, Object tipo) {
               
        try {
            Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost/programacion","root", "");
            Statement sql = conexion.createStatement();
            ResultSet resultado = sql.executeQuery("SELECT * FROM usuarios WHERE usuario='"+usuario+"' AND clave='"+ clave+"' AND tipo = '"+tipo+"'");
        //  System.out.println(resultado);
            if(resultado.first()) {
                return true;
            }
            else {
                return false;
            }            
        } catch (Exception e) {
            return false;
        }        
    }
